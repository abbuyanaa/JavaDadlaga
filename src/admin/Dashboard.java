    private void combo_shop_listActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_combo_shop_listActionPerformed
        String combo_shop_baraa_huwiarilah = combo_shop_list.getSelectedItem().toString();

        try {
            urdun = db.login("select id from shops where sname = '" + combo_shop_baraa_huwiarilah + "'");
            if (urdun.next()) {
                tm_awsan = db.tabled_hewleh("SELECT b.id, s.sname, a.name, c.name, a.too, a.price "
                        + "FROM baraa b, aguulah a, shops s, categories c "
                        + "WHERE b.aid = a.id AND b.sid = s.id AND a.cid = c.id AND s.id = " + urdun.getString("id"));
                tbl_Shopd_awsan_baraa.setModel(tm_awsan);
            } else {
                tm_awsan = db.tabled_hewleh("SELECT b.id, s.sname, a.name, c.name, a.too, a.price "
                        + "FROM baraa b, aguulah a, shops s, categories c "
                        + "WHERE b.aid = a.id AND b.sid = s.id AND a.cid = c.id");
                tbl_Shopd_awsan_baraa.setModel(tm_awsan);
            }
        } catch (SQLException ex) {
            Logger.getLogger(Dashboard.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_combo_shop_listActionPerformed

