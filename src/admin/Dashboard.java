    private void baraaNemehActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_baraaNemehActionPerformed
        bname = stockName.getText().toString().trim();
        btoo = stockToo.getText().toString().trim();
        bprice = stockPrice.getText().toString().trim();

        birsen_date = ((JTextField) stockIrsenDate.getDateEditor().getUiComponent()).getText();
        bduusah_date = ((JTextField) stockDuusahDate.getDateEditor().getUiComponent()).getText();

        getCatName = aguulah_combo_cat_name.getSelectedItem().toString();

        if (bname.isEmpty() || btoo.isEmpty() || bprice.isEmpty() || birsen_date.isEmpty() || bduusah_date.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Алдаа гарлаа.");
        } else {
            try {
                urdun = db.login("select id from categories where name = '" + getCatName + "'");
                if (urdun.next()) {
                    cat_id = urdun.getInt("id");
                }

                db.exec("insert into aguulah (name, too, price, cid, irsen_date, duusah_date)"
                        + "VALUES ('" + bname + "','" + btoo + "','" + bprice + "',"
                        + "'" + cat_id + "','" + birsen_date + "','" + bduusah_date + "')");
                Tbl_Aguulah_Hewleh();
                Aguulah_Combo_Category_Name();
                stockName.setText(null);
                stockToo.setText(null);
                stockPrice.setText(null);
            } catch (SQLException ex) {
                Logger.getLogger(Dashboard.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_baraaNemehActionPerformed

    private void CategoryNemehActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CategoryNemehActionPerformed
        addCatName = catName.getText().toString().trim();
        if (addCatName.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Категори нэмэх утга хоосон байж болохгүй!");
        } else {
            try {
                urdun = db.login("select name from categories where name = '" + addCatName + "'");
                if (urdun.next()) {
                    JOptionPane.showMessageDialog(null, addCatName + " Категори жагсаалтанд байна.");
                } else {
                    db.exec("insert into categories (name) values ('" + addCatName + "')");
                    Tbl_Cat_Hewleh();
                    Aguulah_Combo_Category_Name();
                    catName.setText(null);
                }
            } catch (SQLException ex) {
                Logger.getLogger(Dashboard.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_CategoryNemehActionPerformed

