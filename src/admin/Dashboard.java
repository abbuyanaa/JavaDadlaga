    public void Tbl_Cat_Hewleh() {
        tm_awsan = db.tabled_hewleh("SELECT id as Id, name as 'Category Name' FROM categories ORDER BY Id DESC");
        tbl_category.setModel(tm_awsan);
    }

    public void Tbl_Shop_Hewleh() {
        tm_awsan = db.tabled_hewleh("SELECT id as Id, sname as Name, address as Address FROM shops ORDER BY id DESC");
        tbl_shops.setModel(tm_awsan);
    }

    public void Tbl_Aguulah_Hewleh() {
        tm_awsan = db.tabled_hewleh("SELECT a.id as '№', a.name as 'Барааны нэр', a.too as 'Барааны тоо', a.price as 'Барааны үнэ', "
                + "c.name as 'Төрөл', a.irsen_date as 'Ирсэн өдөр', a.duusah_date as 'Дуусах өдөр'"
                + "FROM aguulah a, categories c WHERE a.cid = c.id ORDER BY a.id DESC");
        tbl_aguulah.setModel(tm_awsan);
    }

    public void Tbl_Stock_Share() {
        tm_awsan = db.tabled_hewleh("SELECT a.id as '№', a.name as 'Барааны нэр', a.too as 'Барааны тоо', a.price as 'Барааны үнэ', "
                + "c.name as 'Төрөл', a.irsen_date as 'Ирсэн өдөр', a.duusah_date as 'Дуусах өдөр'"
                + "FROM aguulah a, categories c WHERE a.cid = c.id");
        tbl_stock_share.setModel(tm_awsan);
    }

    public void Tbl_Shop_huwiarilsan_baraa() {
        tm_awsan = db.tabled_hewleh("SELECT b.id, s.sname, a.name, c.name, a.too, a.price "
                + "FROM baraa b, aguulah a, shops s, categories c "
                + "WHERE b.aid = a.id AND b.sid = s.id AND a.cid = c.id");
        tbl_Shopd_awsan_baraa.setModel(tm_awsan);
    }

    public void Aguulah_Combo_Category_Name() {
        try {
            urdun = db.login("SELECT name FROM categories ORDER BY id DESC");
            while (urdun.next()) {
                aguulah_combo_cat_name.addItem(urdun.getString("name"));
            }
        } catch (SQLException ex) {
            Logger.getLogger(Dashboard.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    public void Combo_Shop_Huwiarlah() {
        try {
            urdun = db.login("SELECT sname FROM shops ORDER BY id DESC");
            while (urdun.next()) {
                combo_shop_list.addItem(urdun.getString("sname").substring(0, 1).toUpperCase() + urdun.getString("sname").substring(1));
            }
        } catch (SQLException ex) {
            Logger.getLogger(Dashboard.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

