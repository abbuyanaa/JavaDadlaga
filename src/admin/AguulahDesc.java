    public AguulahDesc(String get_baraa_id) {
        initComponents();
        ComboCatName();
        this.setLocation(250, 150);

        viewBid.setText("Барааны ID: " + get_baraa_id);
        try {
            urdun = db.login("SELECT a.id, a.name, a.too, a.price, a.cid, a.irsen_date, a.duusah_date "
                    + "FROM aguulah a, categories c WHERE a.cid = c.id and a.id = " + get_baraa_id);
            if (urdun.next()) {
                bname = urdun.getString("name");
                btoo = urdun.getString("too");
                bprice = urdun.getString("price");
                cid = urdun.getString("cid");
                birsen_date = urdun.getString("irsen_date");
                bduusah_date = urdun.getString("duusah_date");

                viewBname.setText(bname);
                viewBtoo.setText(btoo);
                viewBprice.setText(bprice);

                try {
                    Date irsenDate = new SimpleDateFormat("yyyy-MM-dd").parse(birsen_date);
                    Date duusahDate = new SimpleDateFormat("yyyy-MM-dd").parse(bduusah_date);
                    viewIrsenDate.setDate(irsenDate);
                    viewDuusahDate.setDate(duusahDate);
                } catch (ParseException ex) {
                    Logger.getLogger(AguulahDesc.class.getName()).log(Level.SEVERE, null, ex);
                }

            }

            urdun = db.login("select name from categories where id = " + cid);
            if (urdun.next()) {
                viewBcat.setSelectedItem(urdun.getString("name"));
            }
            baraa_id = Integer.parseInt(get_baraa_id);
        } catch (SQLException ex) {
            Logger.getLogger(AguulahDesc.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    public void ComboCatName() {
        try {
            urdun = db.login("select name from categories order by id desc");
            while (urdun.next()) {
                viewBcat.addItem(urdun.getString("name"));
            }
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
    }

