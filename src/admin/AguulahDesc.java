    private void aguulah_baraa_zasahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_aguulah_baraa_zasahActionPerformed
        name = viewBname.getText().toString();
        too = viewBtoo.getText().toString();
        price = viewBprice.getText().toString();

        setIdate = ((JTextField) viewIrsenDate.getDateEditor().getUiComponent()).getText();
        setDdate = ((JTextField) viewDuusahDate.getDateEditor().getUiComponent()).getText();

        try {
            bid = viewBcat.getSelectedItem().toString();
            urdun = db.login("select id from categories where name = '" + bid + "'");
            if (urdun.next()) {
                cid = urdun.getString("id");
            }

            urdun = db.login("select name from aguulah where name = '" + name + "'");
            if (urdun.next()) {
                JOptionPane.showMessageDialog(null, urdun.getString("name") + " бараа байна.");
            } else {
                db.exec("UPDATE aguulah SET name = '" + name + "', "
                        + "too = '" + too + "', "
                        + "price = '" + price + "', "
                        + "cid = '" + cid + "', "
                        + "irsen_date = '" + setIdate + "', "
                        + "duusah_date = '" + setDdate + "' WHERE id = " + baraa_id);
                setVisible(false);
                d.Tbl_Aguulah_Hewleh();
                d.Tbl_Stock_Share();
                d.setVisible(true);
            }
        } catch (SQLException ex) {
            Logger.getLogger(AguulahDesc.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_aguulah_baraa_zasahActionPerformed

